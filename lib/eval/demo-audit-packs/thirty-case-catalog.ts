export type GeneratedCasePack = {
  spec: DemoAuditCaseSpec;
  canonicalBundle: string;
  truthKey: EvidenceStateTruthKey;
};

function gen(
  id: string,
  daNum: string,
  title: string,
  shape: string,
  defendant: string,
  pdfLayout: PdfPageLayoutSpec[],
  canonicalBundle: string,
  truthKey: EvidenceStateTruthKey,
): GeneratedCasePack {
  return {
    spec: { id, title: `DA-${daNum} ${title}`, shape, defendant, pdfLayout },
    canonicalBundle,
    truthKey: { ...truthKey, caseId: id, title: `DA-${daNum} ${title}` },
  };
}

