function pack(
  id: string,
  num: string,
  defendant: string,
  familyTheme: string,
  offenceFamily: string,
  offenceLine: string,
  particulars: string,
  mg5Summary: string,
  servedOnFile: string[],
  outstanding: string[],
  mg6Lines: string[],
  mg11Body: string,
  custodyBody: string,
  listingLine: string,
  indexNotes?: string,
  extraEvidenceItems?: V9BundleSpec["extraEvidenceItems"],
): GeneratedCasePack {
  const spec: V9BundleSpec = {
    caseId: id,
    title: `DA-${num} ${defendant} — ${familyTheme}`,
    defendant,
    urn: `26/DA/${num}`,
    court: "Westbridge Magistrates' Court",
    officer: `DC ${defendant.split(" ").pop()} 44${num}`,
    offenceLine,
    particulars,
    offenceFamily,
    familyTheme,
    mg5Summary,
    servedOnFile,
    outstanding,
    mg6Lines,
    mg11Body,
    custodyBody,
    listingLine,
    indexNotes,
    extraEvidenceItems,
    expectedChaseItems: outstanding.map((s) => s.toLowerCase()),
  };
  return {
    spec: { id, title: spec.title, shape: familyTheme, defendant, pdfLayout: [] },
    canonicalBundle: buildV9CriminalBundle(spec),
    truthKey: buildV9TruthKey(spec),
  };
}

